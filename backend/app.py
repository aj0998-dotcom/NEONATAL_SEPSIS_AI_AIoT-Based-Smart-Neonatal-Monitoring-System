@app.get("/dataset-analytics")
def dataset_analytics():
    if not DATA_PATH.exists():
        return jsonify({"error": "Dataset not found. Run model training first."}), 404

    df = pd.read_csv(DATA_PATH)
    output_counts = df["Output"].value_counts().to_dict()

    feature_histograms = {}
    feature_ranges = {
        "Apgar": (0, 10),
        "HeartRate": (90, 220),
        "Temperature": (34, 42),
        "RespRate": (20, 100),
        "OxygenLevel": (60, 100),
    }

    for feature, (min_v, max_v) in feature_ranges.items():
        counts, bin_edges = np.histogram(df[feature], bins=10, range=(min_v, max_v))
        labels = [f"{bin_edges[i]:.1f}-{bin_edges[i + 1]:.1f}" for i in range(len(bin_edges) - 1)]
        feature_histograms[feature] = {"labels": labels, "counts": counts.tolist()}

    return jsonify(
        {
            "sample_count": int(len(df)),
            "output_distribution": {
                "neg": int(output_counts.get("NEG", 0)),
                "eos": int(output_counts.get("EOS", 0)),
                "los": int(output_counts.get("LOS", 0)),
                "any_sepsis": int(output_counts.get("EOS", 0)) + int(output_counts.get("LOS", 0)),
            },
            "feature_histograms": feature_histograms,
        }
    )


