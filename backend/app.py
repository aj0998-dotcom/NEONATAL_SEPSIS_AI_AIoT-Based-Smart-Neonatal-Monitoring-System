@app.post("/predict")
def predict():
    payload = request.get_json(silent=True) or {}
    try:
        values = [float(payload[key]) for key in FEATURES]
    except (KeyError, TypeError, ValueError):
        return jsonify({"error": "Invalid input. Provide numeric values for all features."}), 400

    apgar, heart_rate, temperature, resp_rate, oxygen = values
    if not (0 <= apgar <= 10):
        return jsonify({"error": "Apgar must be between 0 and 10."}), 400
    if not (60 <= heart_rate <= 220):
        return jsonify({"error": "Heart Rate must be between 60 and 220 bpm."}), 400
    if not (30 <= temperature <= 43):
        return jsonify({"error": "Temperature must be between 30 and 43 deg C."}), 400
    if not (10 <= resp_rate <= 120):
        return jsonify({"error": "Respiratory Rate must be between 10 and 120."}), 400
    if not (40 <= oxygen <= 100):
        return jsonify({"error": "Oxygen Level must be between 40 and 100%."}), 400

    pred_info = _predict_with_types(values)
    pred_label = pred_info["pred_label"]
    probability_any = float(pred_info["any_sepsis_probability"])

    # Keep a binary-ish prediction for existing dashboard logic (1 = any sepsis).
    pred = 0 if pred_label == "NEG" else 1
    if pred_label == "EOS":
        result_text = "Early-onset Sepsis (EOS) risk"
    elif pred_label == "LOS":
        result_text = "Late-onset Sepsis (LOS) risk"
    else:
        result_text = "Low Risk"
    timestamp = datetime.now(timezone.utc).isoformat()
    with _db_conn() as conn:
        conn.execute(
            """
            INSERT INTO predictions
            (created_at, apgar, heart_rate, temperature, resp_rate, oxygen_level, prediction, sepsis_type, result, sepsis_probability)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (
                timestamp,
                apgar,
                heart_rate,
                temperature,
                resp_rate,
                oxygen,
                pred,
                pred_label,
                result_text,
                round(probability_any, 4),
            ),
        )
        conn.commit()
    return jsonify(
        {
            "prediction": pred,
            "sepsis_type": pred_label,
            "result": result_text,
            "sepsis_probability": round(probability_any, 4),
            "class_probabilities": {
                k: round(float(v), 4) for k, v in pred_info["proba_by_label"].items()
            },
            "timestamp": timestamp,
        }
    )


@app.post("/generate-alert")
def generate_alert():
    payload = request.get_json(silent=True) or {}
    try:
        values = [float(payload[key]) for key in FEATURES]
    except (KeyError, TypeError, ValueError):
        return jsonify({"error": "Invalid input. Provide numeric values for all features."}), 400

    apgar, heart_rate, temperature, resp_rate, oxygen = values
    pred_info = _predict_with_types(values)
    pred_label = pred_info["pred_label"]
    probability = float(pred_info["any_sepsis_probability"])
    pred = 0 if pred_label == "NEG" else 1

    if probability > 0.75:
        alert_level = "High"
        alert_message = (
            "High risk of sepsis detected. Immediate neonatal review, sepsis workup, and close monitoring are recommended."
        )
    elif probability >= 0.4:
        alert_level = "Medium"
        alert_message = (
            "Moderate sepsis risk detected. Increase observation frequency and consider laboratory screening."
        )
    else:
        alert_level = "Low"
        alert_message = (
            "Low sepsis risk detected. Continue routine monitoring and standard neonatal care."
        )

    if temperature >= 38.0:
        alert_message += " Fever is present."
    if oxygen < 92:
        alert_message += " Oxygen saturation is reduced."
    if resp_rate > 60:
        alert_message += " Respiratory rate is elevated."
    if apgar < 7:
        alert_message += " Apgar score is below ideal range."

    timestamp = datetime.now(timezone.utc).isoformat()
    with _db_conn() as conn:
        conn.execute(
            """
            INSERT INTO predictions
            (created_at, apgar, heart_rate, temperature, resp_rate, oxygen_level, prediction, sepsis_type, result, sepsis_probability)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (
                timestamp,
                apgar,
                heart_rate,
                temperature,
                resp_rate,
                oxygen,
                pred,
                pred_label,
                (
                    "Early-onset Sepsis (EOS) risk"
                    if pred_label == "EOS"
                    else ("Late-onset Sepsis (LOS) risk" if pred_label == "LOS" else "Low Risk")
                ),
                round(probability, 4),
            ),
        )
        conn.commit()

    return jsonify(
        {
            "prediction": pred,
            "sepsis_type": pred_label,
            "result": (
                "Early-onset Sepsis (EOS) risk"
                if pred_label == "EOS"
                else ("Late-onset Sepsis (LOS) risk" if pred_label == "LOS" else "Low Risk")
            ),
            "sepsis_probability": round(probability, 4),
            "alert_level": alert_level,
            "alert_message": alert_message,
            "class_probabilities": {
                k: round(float(v), 4) for k, v in pred_info["proba_by_label"].items()
            },
            "timestamp": timestamp,
        }
    )


@app.get("/prediction-summary")
def prediction_summary():
    with _db_conn() as conn:
        total_predictions = conn.execute("SELECT COUNT(*) AS c FROM predictions").fetchone()["c"]
        high_count = conn.execute(
            "SELECT COUNT(*) AS c FROM predictions WHERE prediction = 1"
        ).fetchone()["c"]
        low_count = conn.execute(
            "SELECT COUNT(*) AS c FROM predictions WHERE prediction = 0"
        ).fetchone()["c"]
        rows = conn.execute(
            """
            SELECT sepsis_probability
            FROM predictions
            ORDER BY id DESC
            LIMIT 10
            """
        ).fetchall()

    last_probabilities = [float(row["sepsis_probability"]) for row in reversed(rows)]
    last_labels = [f"P{i + 1}" for i in range(len(last_probabilities))]
    return jsonify(
        {
            "total_predictions": total_predictions,
            "high_risk_count": high_count,
            "low_risk_count": low_count,
            "last_probabilities": last_probabilities,
            "last_probability_labels": last_labels,
        }
    )


